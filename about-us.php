                                    En 1901 la Iglesia llega a su sostenimiento propio. El hno. David a. Wilson funda la revista “El Expositor Bíblico.
                                    En 1905 se adquirió el terreno para el templo (una casa sencilla, adaptada para los cultos, clases de Escuela Dominical y Clínica).
                                    El 25 de Diciembre de 1921 se dedicó al servicio de Dios el primer templo, que sirvió por 46 años hasta su demolición el 1 de Mayo de 1968.
                                    El 4 de agosto de 1942 nace el coro “Juan Sebastián Bach” a iniciativa del Pastor Encarnación Salce Sánchez, con 19 integrantes gracias a Dios este coro cumple 81 años de servir al Señor por medio del canto de forma ininterrumpida.
                                    El himno oficial de Convención Nacional Bautista de México se debe a dos hermanos que fueron miembros de nuestra Iglesia: Encarnación Salce Sánchez(letra) y Fernando Gaspar Landero(música).
                                    En 1958 nació el Órgano Informativo de la Iglesia que lleva el nombre de “Boletín” y que se ha publicado cada Domingo sin interrupción (solo en el tiempo de Pandemia en Digital) cumpliendo 64 años informando cada semana a la congregación.
                                    La Iglesia fue escogida para poner el Programa: El plan de Avance Financiero y Espiritual (Mayordomía).
                                    Tuvo por muchos años el “Instituto Permanente  para Laicos” y además se inició el “Calendario de Actividades de Iglesia".
                                    El lunes 8 de Febrero de 1968 se efectuó la colocación de la “Primera Piedra” del nuevo  Templo.
                                    El lunes 18 de Febrero de 1974 (seis años después de la colocación de la “Primera Piedra”), se hizo la dedicación del Templo actual, en un solemne culto de adoración y gratitud a Dios, con la representación de más de 57 Iglesias de la ciudad, el país y del extranjero.
                                    Dios nos concedió en su gran misericordia adquirir con muchos esfuerzos las dos casas contiguas al Templo, por la calle independencia y posteriormente la de Contreras Medellín, aplicándose estos espacios para Planta Educativa, oficinas, baños, cocinas, actividades sociales, bodegas, vivienda para el guarda Templo.
                                    Recientemente El Señor nuestro Dios nos ha concedido que una señorita y dos jóvenes respondieron al llamado del Señor para ir a prepararse a los seminarios.
                                    El 9 septiembre de 1988 El Señor nuestro Dios nos permitió el Primer Centenario y el 9 de septiembre de 2024 como Iglesia celebramos con Júbilo 135 años.
                                    La fachada del Templo fue labrada por uno de nuestros pastores(Octavio Gaspar Landero) lleva el titulo “Decretos Divinos".
